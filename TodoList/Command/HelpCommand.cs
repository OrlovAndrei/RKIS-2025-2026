using System;

public class HelpCommand : ICommand
{
    public void Execute()
    {
        Console.WriteLine(@"СПРАВКА ПО КОМАНДАМ:
    help                    - вывести список команд
    profile                 - показать данные пользователя
    profile --out (-o)      - выйти из текущего профиля
    add ""текст""            - добавить задачу
    add --multiline (-m)    - добавить задачу в многострочном режиме
    view                    - показать только текст задач
    view --index (-i)       - показать с индексами
    view --status (-s)      - показать со статусами
    view --update-date (-d) - показать с датами
    view --all (-a)         - показать всю информацию
    read <номер>            - просмотреть полный текст задачи
    delete <номер>          - удалить задачу
    update <номер> ""текст"" - обновить текст задачи
    status <номер> <статус> - изменить статус задачи
    undo                    - отменить последнее действие
    redo                    - повторить отмененное действие
    
    ПОИСК ЗАДАЧ:
    search [флаги]          - поиск задач с фильтрацией
      --contains ""текст""         - задачи, содержащие текст
      --starts-with ""текст""      - задачи, начинающиеся с текста  
      --ends-with ""текст""        - задачи, заканчивающиеся текстом
      --from ДАТА                  - задачи после даты (yyyy-MM-dd)
      --to ДАТА                    - задачи до даты (yyyy-MM-dd)
      --status СТАТУС              - фильтр по статусу
      --sort text/date             - сортировка по тексту или дате
      --desc                       - сортировка по убыванию
      --top N                      - показать только первые N задач
    
    exit                    - выйти из программы");
    }
    public void Unexecute()
    {
        
    }
}