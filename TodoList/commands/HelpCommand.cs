namespace TodoList
{
    public class HelpCommand : ICommand
    {
        public void Execute()
        {
            System.Console.WriteLine("""
            Доступные команды:
            help - показать список команд
            profile - показать данные пользователя
            profile --out (-o) - выйти из текущего профиля
            
            add "текст задачи" - добавить задачу
            add --multiline (-m) - добавить многострочную задачу (ввод построчно, завершите '!end')
            
            view - показать все задачи (таблица)
            view --index (-i) - показать с номерами
            view --status (-s) - показать с статусом
            view --update-date (-d) - показать с датой последнего изменения
            view --all (-a) - показать все колонки (номер, статус, дата, текст)
            
            read <индекс> - показать полную информацию о задаче
            
            search - поиск задач с фильтрацией и сортировкой
              --contains <текст>   - задачи, содержащие указанный текст
              --starts-with <текст> - задачи, начинающиеся с указанного текста
              --ends-with <текст>   - задачи, заканчивающиеся указанным текстом
              --status <статус>     - NotStarted, InProgress, Completed, Postponed, Failed
              --from <дата>         - задачи с датой изменения не раньше указанной (yyyy-MM-dd)
              --to <дата>           - задачи с датой изменения не позже указанной (yyyy-MM-dd)
              --sort text|date      - сортировка по тексту или дате
              --desc                - сортировка по убыванию (по умолчанию - возрастание)
              --top <n>             - вывести только первые n задач
              --case-sensitive      - регистрозависимый поиск
            
            undo - отменить последнее действие
            redo - повторить отмененное действие
            
            delete <индекс> - удалить задачу
            update <индекс> "новый текст" - обновить текст задачи
            update <индекс> --multiline (-m) - обновить многострочную задачу
            
            status <индекс> <статус> - изменить статус задачи
            
            exit - выйти из программы
            
            Примеры:
              view --all
              view --index --status
              search --contains "купить" --status InProgress
            """);
        }

        public void Unexecute() { }
    }
}